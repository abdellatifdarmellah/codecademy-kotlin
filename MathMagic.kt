fun main() {
  // declaring an immutable variable and assinging it to my name
  val userName = "Abdellatif"

  // declaring a magicNum without initalizing it
  var magicNum: Int

  // multiplying the first digit of my age by 2
  magicNum = 2 * 5

  // adding 3 to the result
  magicNum += 3

  // multiplying the result by 2
  magicNum *= 2

  // adding the second digit of my age to the result
  magicNum += 1

  // subtracting 6 from the result
